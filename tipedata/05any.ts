/**
 * Any artinya sebarang tipe. Variabel dengan tipe data ini bisa diberi sembarang nilai. Sama
   dengan variabel di JavaScript standar.
 */
   //deklarasi
   let ironMan: any;

   //sekarang kita definisikan variabel ironMan dengan berbagai tipe data

   //string
   ironMan = "Tony Stark";

   //number
   ironMan = 1;

   //array
   ironMan = ["marvel", "spiderman", "wonder woman"];

   //boolean
   ironMan = false;

   console.log(ironMan);

   // output false karena typescript akan mengeksekusi variable yang terakhir kali.